'use client';

import { ConnectButton } from '@rainbow-me/rainbowkit';
import { useRouter } from 'next/navigation';
import { useEffect, useState, useCallback, useRef } from 'react';
import { useAccount, useDisconnect } from 'wagmi';
import toast from 'react-hot-toast';

interface ConnectWalletProps {
  onConnected?: () => void;
  redirectPath?: string;
  showWelcomeMessage?: boolean;
  maxRetries?: number;
}

type ConnectionErrorType = 'network' | 'coinbase' | 'timeout' | 'user_rejected' | 'unknown';

interface ConnectionError {
  type: ConnectionErrorType;
  message: string;
  details?: string;
  retryable: boolean;
}

export default function ConnectWallet({ 
  onConnected,
  redirectPath = '/classification',
  showWelcomeMessage = true,
  maxRetries = 3
}: ConnectWalletProps) {
  const router = useRouter();
  const { address, isConnected, isConnecting } = useAccount();
  useDisconnect(); // ‰øùÁïô‰ª•Â§áÂêéÁî®
  const [isRedirecting, setIsRedirecting] = useState(false);
  const [connectionError, setConnectionError] = useState<ConnectionError | null>(null);
  const [retryCount, setRetryCount] = useState(0);
  const [isRetrying, setIsRetrying] = useState(false);
  const timeoutRef = useRef<NodeJS.Timeout | null>(null);
  const mountedRef = useRef(true);

  // Ê∏ÖÁêÜÂáΩÊï∞
  useEffect(() => {
    return () => {
      mountedRef.current = false;
      if (timeoutRef.current) {
        clearTimeout(timeoutRef.current);
      }
    };
  }, []);

  // Ëß£ÊûêÈîôËØØÁ±ªÂûã
  const parseConnectionError = useCallback((error: Error): ConnectionError => {
    const errorMessage = error.message.toLowerCase();
    
    // Coinbase Áõ∏ÂÖ≥ÈîôËØØ
    if (errorMessage.includes('coinbase') || errorMessage.includes('cca-lite')) {
      return {
        type: 'coinbase',
        message: 'Coinbase Èí±ÂåÖÊúçÂä°ÊöÇÊó∂‰∏çÂèØÁî®',
        details: 'ËØ∑Â∞ùËØï‰ΩøÁî®ÂÖ∂‰ªñÈí±ÂåÖÊàñÁ®çÂêéÈáçËØï',
        retryable: true
      };
    }
    
    // ÁΩëÁªúÈîôËØØ
    if (errorMessage.includes('network') || errorMessage.includes('fetch') || errorMessage.includes('timeout')) {
      return {
        type: 'network',
        message: 'ÁΩëÁªúËøûÊé•ÂºÇÂ∏∏',
        details: 'ËØ∑Ê£ÄÊü•ÁΩëÁªúËøûÊé•ÂêéÈáçËØï',
        retryable: true
      };
    }
    
    // Áî®Êà∑ÊãíÁªù
    if (errorMessage.includes('user rejected') || errorMessage.includes('user denied') || errorMessage.includes('canceled')) {
      return {
        type: 'user_rejected',
        message: 'Áî®Êà∑ÂèñÊ∂à‰∫ÜËøûÊé•',
        details: 'ËØ∑ÈáçÊñ∞ÁÇπÂáªËøûÊé•ÊåâÈíÆ',
        retryable: false
      };
    }
    
    // Ë∂ÖÊó∂ÈîôËØØ
    if (errorMessage.includes('timeout')) {
      return {
        type: 'timeout',
        message: 'ËøûÊé•Ë∂ÖÊó∂',
        details: 'Èí±ÂåÖÂìçÂ∫îÊó∂Èó¥ËøáÈïøÔºåËØ∑ÈáçËØï',
        retryable: true
      };
    }
    
    // ÈªòËÆ§Êú™Áü•ÈîôËØØ
    return {
      type: 'unknown',
      message: 'ËøûÊé•Â§±Ë¥•',
      details: error.message,
      retryable: true
    };
  }, []);

  // Â§ÑÁêÜËøûÊé•ÊàêÂäüÂêéÁöÑÈÄªËæë
  const handleConnectionSuccess = useCallback(async () => {
    if (!isConnected || !address || !mountedRef.current) return;

    try {
      setIsRedirecting(true);
      setConnectionError(null);
      setRetryCount(0); // ÈáçÁΩÆÈáçËØïËÆ°Êï∞

      // Ê∏ÖÈô§‰ªª‰ΩïÁé∞ÊúâÁöÑË∂ÖÊó∂
      if (timeoutRef.current) {
        clearTimeout(timeoutRef.current);
      }

      // ÊâßË°åËá™ÂÆö‰πâÂõûË∞É
      if (onConnected) {
        await Promise.race([
          onConnected(),
          new Promise((_, reject) => 
            setTimeout(() => reject(new Error('ËøûÊé•ÂõûË∞ÉË∂ÖÊó∂')), 10000)
          )
        ]);
      }

      // ÊàêÂäüÊèêÁ§∫
      toast.success('Èí±ÂåÖËøûÊé•ÊàêÂäüÔºÅ', {
        duration: 2000,
        icon: 'üéâ'
      });

      // Áü≠ÊöÇÂª∂ËøüÊèêÂçáÁî®Êà∑‰ΩìÈ™å
      if (mountedRef.current) {
        timeoutRef.current = setTimeout(() => {
          if (mountedRef.current) {
            router.push(redirectPath);
          }
        }, 1500);
      }

    } catch (error) {
      console.error('Connection post-processing failed:', error);
      const parsedError = parseConnectionError(error as Error);
      setConnectionError(parsedError);
      setIsRedirecting(false);
      toast.error(parsedError.message);
    }
  }, [isConnected, address, onConnected, router, redirectPath, parseConnectionError]);

  // ÁõëÂê¨ËøûÊé•Áä∂ÊÄÅÂèòÂåñ
  useEffect(() => {
    if (isConnected && address && !isRedirecting && !connectionError) {
      handleConnectionSuccess();
    }
  }, [isConnected, address, isRedirecting, connectionError, handleConnectionSuccess]);

  // Â§ÑÁêÜËøûÊé•ÈîôËØØ
  const handleConnectionError = useCallback((error: Error) => {
    console.error('Wallet connection error:', error);
    const parsedError = parseConnectionError(error);
    setConnectionError(parsedError);
    setIsRedirecting(false);
    setIsRetrying(false);
    
    // ÊòæÁ§∫ÈîôËØØÊèêÁ§∫
    toast.error(parsedError.message, {
      duration: 4000,
      icon: parsedError.type === 'coinbase' ? 'üîó' : '‚ö†Ô∏è'
    });
  }, [parseConnectionError]);

  // Ëá™Âä®ÈáçËØïÈÄªËæë
  const attemptAutoRetry = useCallback(async () => {
    if (retryCount >= maxRetries || !connectionError?.retryable) {
      return;
    }

    setIsRetrying(true);
    setRetryCount(prev => prev + 1);
    
    // Âª∂ËøüÈáçËØïÔºåÈÅøÂÖçÈ¢ëÁπÅËØ∑Ê±Ç
    const delay = Math.min(1000 * Math.pow(2, retryCount), 5000); // ÊåáÊï∞ÈÄÄÈÅøÔºåÊúÄÂ§ß5Áßí
    
    await new Promise(resolve => setTimeout(resolve, delay));
    
    if (mountedRef.current) {
      setConnectionError(null);
      setIsRetrying(false);
      
      toast.loading(`Ëá™Âä®ÈáçËØï‰∏≠... (${retryCount + 1}/${maxRetries})`, {
        duration: 2000,
        icon: 'üîÑ'
      });
    }
  }, [retryCount, maxRetries, connectionError]);

  // ÊâãÂä®ÈáçËØïËøûÊé•
  const handleManualRetry = useCallback(() => {
    setConnectionError(null);
    setRetryCount(0);
    setIsRetrying(false);
    toast.dismiss();
  }, []);

  // Ëá™Âä®ÈáçËØïÊïàÊûú
  useEffect(() => {
    if (connectionError?.retryable && retryCount < maxRetries && connectionError.type !== 'user_rejected') {
      const timeoutId = setTimeout(attemptAutoRetry, 2000);
      return () => clearTimeout(timeoutId);
    }
  }, [connectionError, retryCount, maxRetries, attemptAutoRetry]);

  // Â§ÑÁêÜÈí±ÂåÖËøûÊé•Ë∂ÖÊó∂
  const handleConnectionTimeout = useCallback(() => {
    if (isConnecting) {
      const timeoutError: ConnectionError = {
        type: 'timeout',
        message: 'ËøûÊé•Ë∂ÖÊó∂',
        details: 'Èí±ÂåÖÂìçÂ∫îÊó∂Èó¥ËøáÈïøÔºåËØ∑Ê£ÄÊü•Èí±ÂåÖÁä∂ÊÄÅÊàñÈáçËØï',
        retryable: true
      };
      setConnectionError(timeoutError);
      setIsRetrying(false);
      toast.error('ËøûÊé•Ë∂ÖÊó∂ÔºåËØ∑ÈáçËØï', { icon: '‚è∞' });
    }
  }, [isConnecting]);

  // ËøûÊé•Ë∂ÖÊó∂ÁõëÂê¨
  useEffect(() => {
    if (isConnecting) {
      const timeoutId = setTimeout(handleConnectionTimeout, 30000); // 30ÁßíË∂ÖÊó∂
      return () => clearTimeout(timeoutId);
    }
  }, [isConnecting, handleConnectionTimeout]);

  return (
    <ConnectButton.Custom>
      {({
        account,
        chain,
        openConnectModal,
        authenticationStatus,
        mounted,
      }) => {
        const ready = mounted && authenticationStatus !== 'loading';
        const connected =
          ready &&
          account &&
          chain &&
          (!authenticationStatus ||
            authenticationStatus === 'authenticated');

        return (
          <div
            {...(!ready && {
              'aria-hidden': true,
              'style': {
                opacity: 0,
                pointerEvents: 'none',
                userSelect: 'none',
              },
            })}
            className="space-y-4"
          >
            {(() => {
              // ÊòæÁ§∫ËøûÊé•ÈîôËØØ
              if (connectionError) {
                const getErrorIcon = (type: ConnectionErrorType) => {
                  switch (type) {
                    case 'coinbase': return 'üîó';
                    case 'network': return 'üåê';
                    case 'timeout': return '‚è∞';
                    case 'user_rejected': return '‚ùå';
                    default: return '‚ö†Ô∏è';
                  }
                };

                const getErrorStyles = (type: ConnectionErrorType) => {
                  switch (type) {
                    case 'coinbase': 
                      return {
                        bg: 'bg-orange-50 border-orange-200',
                        text: 'text-orange-800',
                        details: 'text-orange-600',
                        button: 'text-orange-700 hover:text-orange-900 bg-orange-100 hover:bg-orange-200'
                      };
                    case 'network':
                      return {
                        bg: 'bg-blue-50 border-blue-200',
                        text: 'text-blue-800',
                        details: 'text-blue-600',
                        button: 'text-blue-700 hover:text-blue-900 bg-blue-100 hover:bg-blue-200'
                      };
                    case 'timeout':
                      return {
                        bg: 'bg-yellow-50 border-yellow-200',
                        text: 'text-yellow-800',
                        details: 'text-yellow-600',
                        button: 'text-yellow-700 hover:text-yellow-900 bg-yellow-100 hover:bg-yellow-200'
                      };
                    case 'user_rejected':
                      return {
                        bg: 'bg-gray-50 border-gray-200',
                        text: 'text-gray-800',
                        details: 'text-gray-600',
                        button: 'text-gray-700 hover:text-gray-900 bg-gray-100 hover:bg-gray-200'
                      };
                    default:
                      return {
                        bg: 'bg-red-50 border-red-200',
                        text: 'text-red-800',
                        details: 'text-red-600',
                        button: 'text-red-700 hover:text-red-900 bg-red-100 hover:bg-red-200'
                      };
                  }
                };

                const styles = getErrorStyles(connectionError.type);

                return (
                  <div className="space-y-4">
                    <div className={`${styles.bg} border rounded-lg p-4`}>
                      <div className="flex items-start space-x-3">
                        <span className="text-2xl">{getErrorIcon(connectionError.type)}</span>
                        <div className="flex-1">
                          <p className={`${styles.text} font-medium text-sm`}>
                            {connectionError.message}
                          </p>
                          {connectionError.details && (
                            <p className={`${styles.details} text-xs mt-1`}>
                              {connectionError.details}
                            </p>
                          )}
                          
                          {/* ÈáçËØï‰ø°ÊÅØ */}
                          {connectionError.retryable && retryCount < maxRetries && (
                            <div className={`${styles.details} text-xs mt-2 flex items-center space-x-2`}>
                              {isRetrying ? (
                                <>
                                  <div className="animate-spin rounded-full h-3 w-3 border-b border-current"></div>
                                  <span>Ëá™Âä®ÈáçËØï‰∏≠... ({retryCount}/{maxRetries})</span>
                                </>
                              ) : (
                                <span>Â∞ÜÂú® 2 ÁßíÂêéËá™Âä®ÈáçËØï ({retryCount}/{maxRetries})</span>
                              )}
                            </div>
                          )}
                          
                          {/* ÈáçËØïÊåâÈíÆ */}
                          <div className="mt-3 flex space-x-2">
                            {connectionError.retryable && (
                              <button
                                onClick={handleManualRetry}
                                disabled={isRetrying}
                                className={`${styles.button} text-sm font-medium 
                                         disabled:opacity-50 disabled:cursor-not-allowed
                                         px-3 py-1 rounded`}
                              >
                                {isRetrying ? 'ÈáçËØï‰∏≠...' : 'Á´ãÂç≥ÈáçËØï'}
                              </button>
                            )}
                            
                            {connectionError.type === 'coinbase' && (
                              <button
                                onClick={() => {
                                  setConnectionError(null);
                                  toast.success('Âª∫ËÆÆ‰ΩøÁî® MetaMask ÊàñÂÖ∂‰ªñÈí±ÂåÖ', { icon: 'üí°' });
                                }}
                                className="text-gray-600 hover:text-gray-800 text-sm underline"
                              >
                                ‰ΩøÁî®ÂÖ∂‰ªñÈí±ÂåÖ
                              </button>
                            )}
                          </div>
                        </div>
                      </div>
                    </div>
                  </div>
                );
              }

              // ÊòæÁ§∫ÈáçÂÆöÂêëÁä∂ÊÄÅ
              if (connected && isRedirecting) {
                return (
                  <div className="space-y-4 text-center">
                    <div className="bg-green-50 border border-green-200 rounded-lg p-6">
                      <div className="animate-spin rounded-full h-8 w-8 border-b-2 border-green-600 mx-auto mb-4"></div>
                      <p className="text-green-700 font-medium">Èí±ÂåÖËøûÊé•ÊàêÂäüÔºÅ</p>
                      <p className="text-green-600 text-sm mt-1">Ê≠£Âú®Ë∑≥ËΩ¨Âà∞ÂûÉÂúæÂàÜÁ±ªÈ°µÈù¢...</p>
                    </div>
                    {showWelcomeMessage && (
                      <p className="text-sm text-gray-600">
                        Ê¨¢ËøéÂä†ÂÖ•ÁéØ‰øù‰πãÊóÖÔºÅüå±
                      </p>
                    )}
                  </div>
                );
              }

              // ÊòæÁ§∫ËøûÊé•ÊåâÈíÆ
              if (!connected) {
                return (
                  <div className="space-y-4">
                    <button
                      onClick={() => {
                        try {
                          // Ê∏ÖÈô§‰πãÂâçÁöÑÈîôËØØÁä∂ÊÄÅ
                          setConnectionError(null);
                          setRetryCount(0);
                          toast.dismiss();
                          openConnectModal();
                        } catch (error) {
                          handleConnectionError(error as Error);
                        }
                      }}
                      disabled={isConnecting || isRetrying}
                      className={`
                        w-full bg-gradient-to-r from-green-600 to-blue-600 text-white px-8 py-4 rounded-xl text-lg font-semibold 
                        hover:from-green-700 hover:to-blue-700 transition-all transform hover:scale-105 shadow-lg
                        disabled:opacity-50 disabled:cursor-not-allowed disabled:transform-none
                        focus:outline-none focus:ring-2 focus:ring-green-500 focus:ring-offset-2
                      `}
                    >
                      {isConnecting || isRetrying ? (
                        <span className="flex items-center justify-center space-x-2">
                          <div className="animate-spin rounded-full h-4 w-4 border-b-2 border-white"></div>
                          <span>
                            {isRetrying ? `ÈáçËØï‰∏≠... (${retryCount}/${maxRetries})` : 'ËøûÊé•‰∏≠...'}
                          </span>
                        </span>
                      ) : (
                        'üöÄ ÂºÄÂßãÁéØ‰øù‰πãÊóÖ'
                      )}
                    </button>
                    
                    <p className="text-sm text-gray-500 text-center">
                      ËøûÊé•Èí±ÂåÖÂç≥ÂèØÂºÄÂßãÂûÉÂúæÂàÜÁ±ªÊåëÊàò
                    </p>
                    
                    {/* ÊîØÊåÅÁöÑÈí±ÂåÖÊèêÁ§∫ */}
                    <div className="text-xs text-gray-400 text-center space-y-1">
                      <div>ÊîØÊåÅ MetaMask„ÄÅWalletConnect Á≠â‰∏ªÊµÅÈí±ÂåÖ</div>
                      {retryCount > 0 && (
                        <div className="text-yellow-600">
                          ÈÅáÂà∞ÈóÆÈ¢òÔºüÂª∫ËÆÆ‰ΩøÁî® MetaMask Èí±ÂåÖ
                        </div>
                      )}
                    </div>
                    
                    {/* ÁΩëÁªúÁä∂ÊÄÅÊèêÁ§∫ */}
                    {typeof navigator !== 'undefined' && !navigator.onLine && (
                      <div className="bg-yellow-50 border border-yellow-200 rounded-lg p-3">
                        <div className="flex items-center space-x-2 text-yellow-700">
                          <span>üåê</span>
                          <span className="text-sm">ÁΩëÁªúËøûÊé•ÂºÇÂ∏∏ÔºåËØ∑Ê£ÄÊü•ÁΩëÁªúËÆæÁΩÆ</span>
                        </div>
                      </div>
                    )}
                  </div>
                );
              }

              // Â∑≤ËøûÊé•‰ΩÜÊú™ÈáçÂÆöÂêëÔºàÂ§áÁî®Áä∂ÊÄÅÔºâ
              return (
                <div className="space-y-4 text-center">
                  <div className="bg-blue-50 border border-blue-200 rounded-lg p-4">
                    <p className="text-blue-700">Èí±ÂåÖÂ∑≤ËøûÊé•</p>
                    <p className="text-blue-600 text-sm">{account?.displayName}</p>
                  </div>
                </div>
              );
            })()}
          </div>
        );
      }}
    </ConnectButton.Custom>
  );
}

